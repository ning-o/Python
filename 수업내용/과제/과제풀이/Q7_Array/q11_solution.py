# 문제11. [ 수업시간에 학습하지 않은 내용 포함. 구글링을 통해 방법을 찾아 문제를 해결해보는 과제 ] 
# 학교 행사에 참여한 학생들의 명단이 두 반에서 따로 제출되었습니다. 하지만 두 반의 명단에는 중복으로 참여한 학생 이름이 포함되어 있습니다. set(집합)을 이용하여 중복을 제거하고, 공통 참여자와 전체 참여자 명단을 구하는 프로그램을 작성하세요.

# class1 = {"홍길동", "김철수", "이영희", "박민수"}
# class2 = {"이영희", "김철수", "최지훈", "정은비"}
# 출력 예)
# 전체 참여자 명단(중복제거):{'홍길동', '김철수', '이영희', '박민수', '최지훈', '정은비'}
# 두 반 모두 참여한 학생 : {'이영희', '김철수'}
# 1반만 참여한 학생 : {'홍길동', '박민수'}
# 2반만 참여한 학생 : {'최지훈', '정은비'}

# #[HINT]# set의 주요 연산자—-
# 합집합 : |  또는 .union()
# 교집합 : & 또는 .intersection()
# 차집합 : -  또는 .difference()
# —-------------------------------------


class1 = {"홍길동", "김철수", "이영희", "박민수"}
class2 = {"이영희", "김철수", "최지훈", "정은비"}

# 1. 전체 참여자 명단 (합집합: Union)
# 두 집합을 합치되, 중복된 이름은 하나만 남김
total = class1 | class2 
# 또는 class1.union(class2)

# 2. 두 반 모두 참여한 학생 (교집합: Intersection)
# 양쪽 집합에 모두 존재하는 이름만 추출
common = class1 & class2
# 또는 class1.intersection(class2)

# 3. 1반만 참여한 학생 (차집합: Difference)
# class1에는 있지만 class2에는 없는 이름을 남김.
only_class1 = class1 - class2
# 또는 class1.difference(class2)

# 4. 2반만 참여한 학생 (차집합: Difference)
# class2에는 있지만 class1에는 없는 이름을 남김.
only_class2 = class2 - class1
# 또는 class2.difference(class1)

# 결과 출력
print("전체 참여자 명단(중복제거):", total)
print("두 반 모두 참여한 학생 :", common)
print("1반만 참여한 학생 :", only_class1)
print("2반만 참여한 학생 :", only_class2)