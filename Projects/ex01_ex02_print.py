# python은 자동으로 줄을 바꿔줌 print()마지막에 \n이 포함되어 있음. 이걸 바꿀 수 있는 기능
print(10, end=" ")  # 끝에 한줄 띄고
print(20, end='0')  # 끝에 0을 붙이고
print(30, end="")   # 끌에 줄바꿈도 띄어쓰기도 하지 않음
print(40, end="\n") # 기본형, end= 를 쓰지 않아도 적용

