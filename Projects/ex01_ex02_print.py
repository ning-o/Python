# 그래서 등장한 문자열의 특별한 서식 만들기 기능. format()

print("나는 {} 입니다.".format("홍길동"))
print("당신의 성적은 {}점 입니다.".format(80+3))
print("나의 이름은 {}이고, 나이는 {}살 입니다.".format('sam',20))
print("{} X {} = {}".format(4,1,4*1))
# 주의.. {}의 개수와 format()에 전다라는 값들의 개수는 같아야 함.
# print("{} - {}".format(174)) ---error
print("{} - {}".format(174,72,20)) # {}보다 값의 개수가 많으면 에러 아님.. 마지막 값만 소실 됨.
print("-"*30)
print()

# python 3버전 이상부터.. 등장한 f-string 포멧 기술을 사용함.
print(f"{100+3}점 입니다.") # 중괄호 안에 영역은 문자열이 아닌 Data영역으로 봄.
print("100+3점 입니다.")
print(f"{8} x {1} = {8*1}") # 값을 뒤에 넣는 게 아닌, 바로 넣을 수 있음
print(f"나의 이름은 {"홍길동"}입니다.") # 문자열은 똑같이 따옴표 사용

