# 반복문 과제
# 1.
# 프로그램 사용자로부터 양의 정수를 하나 입력 받아서, 그 수만큼 "Hello World!"를 출력하는 프로그램을 작성해보자.

for q1_a in range(int(input('#1.정수 입력: '))):
    print('Hello')
print('-'*30,'\n')

# 2.
# 프로그램 사용자로부터 양의 정수를 하나 입력 받은 다음, 그 수만큼 3의 배수를 
# 출력하는 프로그램을 작성해 보자. 예를 들어 5를 입력받았다면, 3 6 9 12 15를 
# 출력해야 하고 3을 입력받았다면, 3 6 9 를 출력해야 한다.
q2_input=int(input('#2.정수 입력: '))
for a2_a in range(1,q2_input+1):
    print(a2_a*3)
print('-'*30,'\n')

# 3.
# 프로그램 사용자로부터 계속해서 정수를 입력받는다. 그리고 그 값을 계속해서 더해 나간다.  
# 이러한 작업은 프로그램 사용자가 0을 입력할 때까지 계속되어야 하며, 0이 입력되면 
# 입력된 모든 정수의 합을 출력하고 프로그램을 종료한다.

q3_total= 0

while True:
    q3_input= int(input('#3.정수 입력: '))
    print(q3_input+q3_input)
    q3_total += q3_input

    if q3_input==0:
        print(f'입력된 수의 총합: {q3_total}')
        break
print('-'*30,'\n')

# 4.
# 프로그램 사용자로부터 입력 받은 숫자에 해당하는 구구단을 출력하되, 역순으로 출력하는 프로그램을 작성해 보자.
q4_dan= int(input('#4.단수 입력: '))
    
for q4_a in range(9,0,-1):
    print(f'{q4_dan} x {q4_a} = {q4_dan*q4_a}')
