print("-"*30)
print()

# 문자 선택 연산자(인덱싱): []
print("문자 선택 연산자에 대해 알아볼까요?")
print("안녕하세요"[0])
print("안녕하세요"[1])
print("안녕하세요"[4])
# 0부터 시작하므로 0번째가 '안'이 되고 4번째가 '요'가 된다.

# 뒤에서부터 선택하기
print("안녕하세요"[-1])
print("안녕하세요"[-3])

# 문자열 범위 선택 연산자(슬라이싱): [:]
print("안녕하세요"[1:4]) #1부터 3까지 --파이썬은 마지막 숫자를 포함하지 않음.
print("안녕하세요"[0:5]) #0부터 4까지
print("안녕하세요"[1:3])

