# \t 는 고정적인 사이즈는 아님. 다음 탭 간격으로 커서를 이동해라..[통상적인 1탭의 간격은 8칸]
print("a\tb") # a가 표시되는 1탭의 간격이 8칸.. 그 후 b를 표시 [이 상황에서의 \t는 7칸 간격]
print("aa\tb") # [이 상황에서의 \t는 6칸 간격]
print("aaaaaa\tb") # [이 상황에서의 \t는 2칸 간격]
print("aaaaaaaa\tb") # [이 상황에서의 \t는 문자가 이미 8칸을 차지했기 때문에 새롭게 8칸 간격]
print("홍길동전\tb") # 한글은 한 글자각 2개의 자리를 차지해서 4글자=8칸
print()


